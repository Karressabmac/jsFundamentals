//Assignment operator
let x = 20;

//addition assignment operator
x += 1; //x = x + 1
console.log(x);

//subtraction assignment operator
x -= 1; //x = x-1
console.log(x);

//multiplication assignment operator
x *= 2; //x = x * 2
console.log(x);

//division assignmennt operator
x /= 4 //x = x / 4
console.log(x);

//remainder (modulus) assignment operator
x %= 2
console.log(x);

//modulus operator - divides numbers and gives you the 
console.log(5%3); //5/3 gives us 1 2/3 -> 2
console.log(25%7); // 25/7 (twenty five divided by 7) = 3 4/7 -> 4

//exponential assignment operator
x **= 2; // Take the value of 'x' and raise it to the power of 2
console.log(x); 

let y = 7;
y **= 3;
console.log(y); // Take the value of 'y' and raise it to the power of 3